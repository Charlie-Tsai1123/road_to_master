#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 3.69;
    double y = 4;
    double z;

    cout << "max(x, y) = " << max(x, y) << endl;
    cout << "min(x, y) = " << min(x, y) << endl;
    cout << "pow(2, 3) = " << pow(2, 3) << endl;
    cout << "sqrt(9) = " << sqrt(9) << endl;
    cout << "abs(-3) = " << abs(-3) << endl;
    cout << "round(x) = " << round(x) << endl;
    cout << "ceil(x) = " << ceil(x) << endl;
    cout << "floor(x) = " << floor(x) << endl;
}